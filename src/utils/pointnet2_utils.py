class pointnet_encoder(nn.Module):
    def __init__(self, output_size=32):
        super(pointnet_encoder, self).__init__()
        self.conv1 = nn.Conv1d(3, 32, 1)
        self.conv2 = nn.Conv1d(32, output_size, 1)
        self.conv3 = nn.Conv1d(output_size*2, output_size, 1)
        self.conv4 = nn.Conv1d(output_size, output_size, 1)

    def forward(self, x):
        x = x.permute(0,2,1)
        
        batch_size, _, num_points = x.size()
        
        x = self.conv1(x)
        x = F.relu(x) # 128

        x = self.conv2(x) # 256
        global_feature, _ = torch.max(x, 2) #256
        x = torch.cat((x, global_feature.view(batch_size, -1, 1).repeat(1, 1, num_points).contiguous()), 1) #512

        x = self.conv3(x)
        x = F.relu(x) # 512

        x = self.conv4(x)
        global_feature, _ = torch.max(x, 2)
        return global_feature.view(batch_size, -1), x.permute(0,2,1)

class pointnet_decoder(nn.Module):
    def __init__(self, pts_feats_num=32, num_coarse=160):
        super(pointnet_decoder, self).__init__()
        self.num_coarse = num_coarse
        self.fc1 = nn.Linear(pts_feats_num, 2*pts_feats_num)
        self.fc2 = nn.Linear(2*pts_feats_num, 4*pts_feats_num)
        self.fc3 = nn.Linear(4*pts_feats_num, num_coarse * 3)

    def forward(self, x):
        coarse = F.relu(self.fc1(x))
        coarse = F.relu(self.fc2(coarse))
        coarse = self.fc3(coarse).view(-1, self.num_coarse, 3)
        return coarse
    
class Embedder:
    def __init__(self, **kwargs):
        self.kwargs = kwargs
        self.create_embedding_fn()

    def create_embedding_fn(self):
        embed_fns = []
        d = self.kwargs['input_dims']
        out_dim = 0
        if self.kwargs['include_input']:
            embed_fns.append(lambda x : x)
            out_dim += d

        max_freq = self.kwargs['max_freq_log2']
        N_freqs = self.kwargs['num_freqs']

        if self.kwargs['log_sampling']:
            freq_bands = 2.**torch.linspace(0., max_freq, steps=N_freqs)
        else:
            freq_bands = torch.linspace(2.**0., 2.**max_freq, steps=N_freqs)
        for freq in freq_bands:
            for p_fn in self.kwargs['periodic_fns']:
                embed_fns.append(lambda x, p_fn=p_fn, freq=freq : p_fn(x * freq))
                out_dim += d

        self.embed_fns = embed_fns
        self.out_dim = out_dim

    def embed(self, inputs):
        return torch.cat([fn(inputs) for fn in self.embed_fns], -1)
    
def get_embedder(multires, i=0):
    if i == -1:
        return nn.Identity(), 3

    embed_kwargs = {
                'include_input' : True,
                'input_dims' : 3,
                'max_freq_log2' : multires-1,
                'num_freqs' : multires,
                'log_sampling' : True,
                'periodic_fns' : [torch.sin, torch.cos],
    }

    embedder_obj = Embedder(**embed_kwargs)
    embed = lambda x, eo=embedder_obj : eo.embed(x)
    return embed, embedder_obj.out_dim

class weight_decoder(nn.Module):
    def __init__(self, fr_xyz=15, pts_feat_num=32):
        super(weight_decoder, self).__init__()
        self.pe_xyz, input_ch = get_embedder(fr_xyz, 0)
        self.xyz_linear = nn.Linear(input_ch, pts_feat_num)
        self.fc1 = nn.Linear(pts_feat_num * 2, 32)
        self.fc2 = nn.Linear(32, 2)

    def forward(self, xyz, pts_feats):
        input_xyz = self.pe_xyz(xyz)
        input_xyz = self.xyz_linear(input_xyz)
        h = torch.cat((input_xyz, pts_feats), dim=-1)
        
        h = F.relu(self.fc1(h))
        h = F.softmax(self.fc2(h), dim=-1)
        return h
    
